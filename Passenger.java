public class Passenger {
    //variables for the customer details
    public String firstName1;
    public String firstName2;
    public String firstName3;
    public String lastName1;
    public String lastName2;
    public String lastName3;
    public String expenses1;
    public String expenses2;
    public String expenses3;
    public int totalExpense;

    public Passenger(String firstName1, String firstName2, String firstName3, String lastName1, String lastName2, String lastName3, String expenses1, String expenses2, String expenses3, int totalExpense) {
        this.firstName1 = firstName1;
        this.firstName2 = firstName2;
        this.firstName3 = firstName3;
        this.lastName1 = lastName1;
        this.lastName2 = lastName2;
        this.lastName3 = lastName3;
        this.expenses1 = expenses1;
        this.expenses2 = expenses2;
        this.expenses3 = expenses3;
        this.totalExpense = totalExpense;
    }
}
